const calculator = {
    sum(ultima, school) {
        return ultima + school;
    },
 
   
    subtract(ultima, school) {
        return ultima - school;
    },
    
    divide(ultima, school){
        return ultima / school;
    },
    
    multiply(ultima, school) {
        return ultima * school;
    },
}

module.exports = {
    calculator
};