const calculator = {
    sum(ultma, school) {
        return ultma + school;
    },
 
   
    subtract(ultma, school) {
        return ultma - school;
    },
    
    divide(ultma, school){
        return ultma / school;
    },
    
    multiply(ultma, school) {
        return ultma * school;
    },
}

module.exports = {
    calculator
};